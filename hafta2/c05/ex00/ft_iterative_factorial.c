int	ft_iterative_factorial(int nb)
{
	int	res;

	res = 1;
	if (nb == 0)
		return (1);
	else if (nb < 0)
		return (0);
	while (nb)
	{
		res *= nb;
		nb--;
	}
	return (res);
}

int	main(void)
{
	ft_iterative_factorial(5);
}
