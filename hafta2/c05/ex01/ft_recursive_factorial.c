int	ft_recursive_factorial(int nb)
{
	if (nb == 0)
		return (1);
	else if (nb < 0)
		return (0);

	return (nb * ft_recursive_factorial(nb - 1));
}

int	main(void)
{
	int res = ft_recursive_factorial(5);
	(void)res;
}