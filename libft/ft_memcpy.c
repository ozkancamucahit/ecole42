#include <string.h>
#include <stdio.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t			idx;
	unsigned char	*dest_buff;
	const unsigned char	*src_buff;

	idx = 0;
	if (dest == NULL || src == NULL)
		return (NULL);
	dest_buff = (unsigned char *)dest;
	src_buff = (const unsigned char *)src;
	while (idx < n)
	{
		dest_buff[idx] = src_buff[idx];
		idx++;
	}
	return (dest);
}

void	*ft_memcpyOK(void *dst, const void *src, size_t n)
{
	char		*dstc;
	const char	*srccc;

	if ((dst == src) || n == 0)
		return (dst);
	if (!dst && !src)
		return (0);
	dstc = (char *)dst;
	srccc = (const char *)src;
	while (n--)
		dstc[n] = srccc[n];
	return (dst);
}


