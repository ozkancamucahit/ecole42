#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*buff;
	unsigned int	idx;

	idx = 0;
	if (s == 0)
		return ;
	buff = (unsigned char *)s;
	while (idx < n)
		buff[idx++] = 0;
}
