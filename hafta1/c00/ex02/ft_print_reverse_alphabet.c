#include <unistd.h>

void	ft_print_reverse_alphabet(void)
{
	int	ascii_end_code;

	ascii_end_code = 122;
	while (ascii_end_code >= 97)
	{
		write(1, &ascii_end_code, 1);
		ascii_end_code--;
	}
}

int	main(void)
{
	ft_print_reverse_alphabet();
	return (0);
}
