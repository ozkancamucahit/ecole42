int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len] != '\0')
		len++;
	return (len);
}

unsigned int	ft_strlcat(char *dest, char *src, unsigned int size)
{
	char	*d;
	char	*s;
	unsigned int	dlen;
	unsigned int	n;

	n = size;
	s = src;
	d = dest;
	while (n != 0 && *d != '\0')
	{
		d++;
		n--;
	}
	dlen = d - dest;
	n = size - dlen;
	if (n == 0)
		return (dlen + ft_strlen(s));
	while (*s != '\0')
	{
		if (n != 1)
		{
			*d = *s;
			d++;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dlen + (s - src));	
}